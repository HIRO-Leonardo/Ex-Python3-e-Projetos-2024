
def metade(moeda):
    met = moeda / 2
    return met

def dobro(moeda):
    dob = moeda * 2
    return dob

def menos13(moeda, m):
    m_total = m / 100 
    menos = moeda * m_total
    total_menos = moeda - menos
    return total_menos

def aumento10(moeda, a):
    a_total = a / 100 
    aum = moeda * a_total
    total = moeda + aum
    return total

